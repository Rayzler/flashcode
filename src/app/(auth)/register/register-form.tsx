"use client";

import { useState } from "react";
import { signIn } from "next-auth/react";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import { Loader2 } from "lucide-react";
import { registerUser } from "@/app/actions/auth";

export function RegisterForm() {
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);

  const handleGoogleSignIn = async () => {
    setIsLoading(true);
    await signIn("google", { callbackUrl: "/editor" });
  };

  const handleSubmit = async (e: React.FormEvent<HTMLFormElement>) => {
    e.preventDefault();
    setIsLoading(true);
    setError(null);

    const formData = new FormData(e.currentTarget);
    const email = formData.get("email") as string;
    const password = formData.get("password") as string;
    const name = formData.get("name") as string;

    try {
      const result = await registerUser({ email, password, name });

      if (!result.success) {
        setError(result.error || "Failed to create account");
        setIsLoading(false);
        return;
      }

      // Auto sign in después del registro
      await signIn("credentials", {
        email,
        password,
        callbackUrl: "/editor"
      });
    } catch (error) {
      console.error("Registration error:", error);
      setError("Something went wrong. Please try again.");
      setIsLoading(false);
    }
  };

  return (
    <div className="bg-[#14181F] border border-[#2D3340] rounded-lg p-6 space-y-6">
      {/* Google Sign Up */}
      <Button
        type="button"
        variant="outline"
        className="w-full bg-[#1C2229] border-[#2D3340] hover:bg-[#1C2229]/80 text-[#E6E8EB]"
        onClick={handleGoogleSignIn}
        disabled={isLoading}
      >
        {isLoading ? (
          <Loader2 className="w-4 h-4 mr-2 animate-spin" />
        ) : (
          <svg className="w-4 h-4 mr-2" viewBox="0 0 24 24">
            <path
              fill="currentColor"
              d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
            />
            <path
              fill="currentColor"
              d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
            />
            <path
              fill="currentColor"
              d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
            />
            <path
              fill="currentColor"
              d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
            />
          </svg>
        )}
        Continue with Google
      </Button>

      {/* Divider */}
      <div className="relative">
        <div className="absolute inset-0 flex items-center">
          <span className="w-full border-t border-[#2D3340]" />
        </div>
        <div className="relative flex justify-center text-xs uppercase">
          <span className="bg-[#14181F] px-2 text-[#ACB2BD]">
            Or continue with email
          </span>
        </div>
      </div>

      {/* Registration Form */}
      <form onSubmit={handleSubmit} className="space-y-4">
        {error && (
          <div className="bg-[#EF4444]/10 border border-[#EF4444]/20 rounded-md p-3">
            <p className="text-sm text-[#EF4444]">{error}</p>
          </div>
        )}

        <div className="space-y-2">
          <Label htmlFor="name" className="text-[#E6E8EB]">
            Name
          </Label>
          <Input
            id="name"
            name="name"
            type="text"
            placeholder="John Doe"
            required
            disabled={isLoading}
            className="bg-[#1C2229] border-[#2D3340] text-[#E6E8EB] placeholder:text-[#6C7380]"
          />
        </div>

        <div className="space-y-2">
          <Label htmlFor="email" className="text-[#E6E8EB]">
            Email
          </Label>
          <Input
            id="email"
            name="email"
            type="email"
            placeholder="you@example.com"
            required
            disabled={isLoading}
            className="bg-[#1C2229] border-[#2D3340] text-[#E6E8EB] placeholder:text-[#6C7380]"
          />
        </div>

        <div className="space-y-2">
          <Label htmlFor="password" className="text-[#E6E8EB]">
            Password
          </Label>
          <Input
            id="password"
            name="password"
            type="password"
            placeholder="••••••••"
            required
            minLength={8}
            disabled={isLoading}
            className="bg-[#1C2229] border-[#2D3340] text-[#E6E8EB] placeholder:text-[#6C7380]"
          />
          <p className="text-xs text-[#ACB2BD]">
            Must be at least 8 characters
          </p>
        </div>

        <Button
          type="submit"
          className="w-full bg-[#6C63FF] hover:bg-[#5B52FF] text-white"
          disabled={isLoading}
        >
          {isLoading ? (
            <>
              <Loader2 className="w-4 h-4 mr-2 animate-spin" />
              Creating account...
            </>
          ) : (
            "Create account"
          )}
        </Button>
      </form>
    </div>
  );
}
